    private void tbarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbarangMouseClicked
        int bar = tbarang.getSelectedRow();
        ekode = tabmodel.getValueAt(bar, 0).toString();
        enama = tabmodel.getValueAt(bar, 1).toString();
        ejumlah = tabmodel.getValueAt(bar, 2).toString();
        emodal = tabmodel.getValueAt(bar, 3).toString();
        eharga = tabmodel.getValueAt(bar, 4).toString();
        if (edittombol == 1){
            tfkode.setText(ekode);
            tfnama.setText(enama);
            tfjumlah.setText(ejumlah);
            tfmodal.setText(emodal);
            tfharga.setText(eharga);
        } else {
            if (editprompt == 0){
                JOptionPane.showMessageDialog(null,
                    "Klik Tombol Edit untuk meng-enable bagan edit");
                editprompt = 1;
            }
        }
        
        
    }//GEN-LAST:event_tbarangMouseClicked

    private void jbdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbdeleteActionPerformed
        int ok = JOptionPane.showConfirmDialog(null,
                "Anda yakin ingin menghapus data?",
                "Konfirmasi",
                JOptionPane.YES_NO_OPTION);
        if (ok==0){
            String sql = "delete from barang where " + "kode_barang='"+ekode+"'";
            try{
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null,
                       "Data berhasil dihapus");
                dataTable();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data gagal dihapus"+e);
            }
        }
    }//GEN-LAST:event_jbdeleteActionPerformed

