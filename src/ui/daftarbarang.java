    private void jbsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbsaveActionPerformed
        String s_jum = tfjumlah.getText();
        int jum = Integer.parseInt(s_jum);
        if(jum >= 0){
            try{
                String sql = "update barang set nama_barang=?,"+" jumlah=?,"+" modal=?,"+" harga=?"+" where kode_barang='"+tfkode.getText()+"'";
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, tfnama.getText());
                int jumlah = Integer.valueOf(tfjumlah.getText());
                stat.setInt(2, jumlah);
                int modal = Integer.valueOf(tfmodal.getText());
                stat.setInt(3, modal);
                int harga = Integer.valueOf(tfharga.getText());
                stat.setInt(4, harga);

                stat.executeUpdate();
                JOptionPane.showMessageDialog(null,
                        "Data Berasil disimpan");
                kosong();
                dataTable();

            }
            catch(SQLException e) {
                JOptionPane.showMessageDialog(null,
                        "Data gagal diubah"+e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Nilai Jumlah Tidak Boleh Negatif");
        }
    }//GEN-LAST:event_jbsaveActionPerformed

