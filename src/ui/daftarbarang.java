    protected void dataTable() throws SQLException{
        Object[] baris = {"Kode Barang", "Nama Barang", "Jumlah", "Modal", "Harga"};
        tabmodel = new DefaultTableModel(null, baris);
        tbarang.setModel(tabmodel);
        String sql = "select * from barang";
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String skdbrg = hasil.getString("kode_barang");
                String snmbrg = hasil.getString("nama_barang");
                int sjum = hasil.getInt("jumlah");
                int smod = hasil.getInt("modal");
                int shar = hasil.getInt("harga");
                tabmodel.addRow(new Object[]{skdbrg, snmbrg, sjum, smod, shar});
            }
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null,
                    "Data Tidak Bisa Ditampilkan"+e);
        }
    
    }
