    private void isicombobox(){
        cb_merk.removeAllItems();
        cb_tipe.removeAllItems();
        try{
            String sql = "select nama_merk from merk";
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            cb_merk.addItem("");
            while(hasil.next()){
                String nm_merk = hasil.getString("nama_merk");
                cb_merk.addItem(nm_merk);
            }
            
            String sqltipe = "select nama_tipe from tipe_barang";
            ResultSet hasiltipe = stat.executeQuery(sqltipe);
            cb_tipe.addItem("");
            while(hasiltipe.next()){
                String nm_tipe = hasiltipe.getString("nama_tipe");
                cb_tipe.addItem(nm_tipe);
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    private void getid(){
        String nm_merk = (String)cb_merk.getSelectedItem();
        String nm_tipe = (String)cb_tipe.getSelectedItem();
        try{
            String sql = "select id_merk from merk where nama_merk='"+nm_merk+"'";
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                id_merk = hasil.getString("id_merk");
            }
            String sql_tipe = "select id_tipe from tipe_barang where nama_tipe='"+nm_tipe+"'";
            java.sql.Statement stat_tipe = conn.createStatement();
            ResultSet hasil_tipe = stat.executeQuery(sql_tipe);
            while(hasil_tipe.next()){
                id_tipe = hasil_tipe.getString("id_tipe");
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    private void isikodebrg(){
        String kode_brg = "";
        kode_brg = id_merk + id_tipe;
        int hasil_count = 0;
        try{
            String check_sql = "select kode_barang from barang where kode_barang LIKE '"+kode_brg+"%'";
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil_check = stat.executeQuery(check_sql);
            while(hasil_check.next()){
                hasil_count++;
            }
            hasil_count++;
            if(hasil_count < 10){
                kode_brg = kode_brg+"00"+hasil_count;
            }else if(hasil_count > 9 && hasil_count < 100){
                kode_brg = kode_brg+"0"+hasil_count;
            } else if (hasil_count > 99){
                kode_brg = kode_brg+hasil_count;
            }
            
            tfkdbrg.setText(kode_brg);
            
            if((id_merk.equals(""))||(id_tipe.equals(""))){
                tfkdbrg.setText("");
            }
            String nm_merk = (String)cb_merk.getSelectedItem();
            String nm_tipe = (String)cb_tipe.getSelectedItem();
            tfnmbrg.setText(nm_tipe+" "+nm_merk);
            
        } catch(SQLException ex) {
                JOptionPane.showMessageDialog(null,ex);
        }
    }
