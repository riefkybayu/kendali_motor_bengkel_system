    private void bsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsaveActionPerformed
        String sql = "insert into barang values (?,?,?,?,?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, tfkdbrg.getText());
            stat.setString(2, tfnmbrg.getText());
            int tempint;
            if(!(tfjumlah.getText().equals(""))){
                tempint = Integer.parseInt(tfjumlah.getText());
                stat.setInt(3, tempint);
            }
            if(!(tfmodal.getText().equals(""))){
                tempint = Integer.parseInt(tfmodal.getText());
                stat.setInt(4, tempint);
            }
            if(!(tfharga.getText().equals(""))){
                tempint = Integer.parseInt(tfharga.getText());
                stat.setInt(5, tempint);
            }
            stat.setString(6, id_merk);
            stat.setString(7, id_tipe);
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,
                    "Data Berasil disimpan");
            kosong();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Pastikan semua kolom diisi dengan benar");
        }
    }//GEN-LAST:event_bsaveActionPerformed

